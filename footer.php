  <script type="text/javascript"> 
     $(document).ready(function() {
    // Get the current URL path
    var path = window.location.pathname;
    
    // Extract the filename from the path
    var page = path.split("/").pop();
    
    // Remove any existing "active" class from menu items and sub-menu items
    $("ul li.rd-nav-item, ul li.rd-nav-item ul.sub-menu li").removeClass("active");
    
    // Add "active" class to main menu item corresponding to the current page
    $("ul li.rd-nav-item").each(function() {
      var href = $(this).find("a").attr("href");
      if (href == page) {
        $(this).addClass("active");
      }
    });
    
    // Add "active" class to sub-menu item corresponding to the current page
    $("ul li.rd-nav-item ul.sub-menu li").each(function() {
      var href = $(this).find("a").attr("href");
      if (href == page) {
        $(this).addClass("active");
        $(this).closest(".rd-nav-item").addClass("active"); // Add "active" class to parent menu item
      }
    });
  });
  </script>


